n = (3* 3125**9) + (2* 625**8) - (4* 625**7) + (3* 125**6) - (2* 25**5) - 2024

res = ''
while n!=0:
    b = n%25
    if b > 9:
        res += '*'
    else:
        res+=str(b)
    n//=25
res = res[::-1]

# что это хз, но вроде те нули, которые нельзя убирать без числа
# незначащие: 000050 = 50
# значащие: 500000 = 500000
print(res.count('0'))